#include "libft.h"

int	ft_str_is_digit(char *str)
{
	int	i;

	i = 0;
	if (str[i] == 43 || str[i] == 45)
		i++;
	while (str[i] != '\0')
	{
		if (str[i] < 48 || str[i] > 57)
			return (0);
		i++;
	}
	if ((str[0] == 43 || str[0] == 45) && i == 1)
		return (0);
	return (1);
}
